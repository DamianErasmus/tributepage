//luffy
const luffyCard = document.getElementById("luffy");
const luffyFlag = document.getElementById("luffy_flag");
const luffyText = document.getElementById("luffy_text");

luffyCard.onmouseenter = function () {
    luffyFlag.style.display = "none";
    luffyText.style.display = "block";
}
luffyCard.onmouseleave = function () {
    luffyText.style.display = "none";
    luffyFlag.style.display = "block";
}



